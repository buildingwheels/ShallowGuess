#[allow(dead_code)]
pub const fn get_white_pawn_attack_mask(pawn_mask: BitBoard) -> BitBoard {
    (pawn_mask << 7) & !FILE_MASKS[FILE_H] | (pawn_mask << 9) & !FILE_MASKS[FILE_A]
}

#[allow(dead_code)]
pub const fn get_black_pawn_attack_mask(pawn_mask: BitBoard) -> BitBoard {
    (pawn_mask >> 7) & !FILE_MASKS[FILE_A] | (pawn_mask >> 9) & !FILE_MASKS[FILE_H]
}

pub const fn get_bishop_attack_mask(occupy_mask: BitBoard, from_square: ChessSquare) -> BitBoard {
    let mut up_left_attack_mask = UP_LEFT_RAY_MASKS[from_square];

    if up_left_attack_mask & occupy_mask != 0 {
        let blocker_index = get_lowest_occupied_chess_square(up_left_attack_mask & occupy_mask);
        up_left_attack_mask &= !UP_LEFT_RAY_MASKS[blocker_index];
    }

    let mut up_right_attack_mask = UP_RIGHT_RAY_MASKS[from_square];

    if up_right_attack_mask & occupy_mask != 0 {
        let blocker_index = get_lowest_occupied_chess_square(up_right_attack_mask & occupy_mask);
        up_right_attack_mask &= !UP_RIGHT_RAY_MASKS[blocker_index];
    }

    let mut down_left_attack_mask = DOWN_LEFT_RAY_MASKS[from_square];

    if down_left_attack_mask & occupy_mask != 0 {
        let blocker_index = get_highest_occupied_chess_square(down_left_attack_mask & occupy_mask);
        down_left_attack_mask &= !DOWN_LEFT_RAY_MASKS[blocker_index];
    }

    let mut down_right_attack_mask = DOWN_RIGHT_RAY_MASKS[from_square];

    if down_right_attack_mask & occupy_mask != 0 {
        let blocker_index = get_highest_occupied_chess_square(down_right_attack_mask & occupy_mask);
        down_right_attack_mask &= !DOWN_RIGHT_RAY_MASKS[blocker_index];
    }

    up_left_attack_mask | up_right_attack_mask | down_left_attack_mask | down_right_attack_mask
}

